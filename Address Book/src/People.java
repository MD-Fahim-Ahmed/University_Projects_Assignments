class People {
    
    String name;
    String phone;
    String email;
    String address;
    
    public People(String name,String phone,String email,String address){
        this.name=name;
        this.phone=phone;
        this.email=email;
        this.address=address;
    }

    public void setName(String name){
        this.name=name;
    }
    
   
    public void setPhone(String phone){
        this.phone=phone;
    }
   
    public void setEmail(String email){
        this.email=email;
    }
 
    public void setAddress(String address){
        this.address=address;
    }
    
}
